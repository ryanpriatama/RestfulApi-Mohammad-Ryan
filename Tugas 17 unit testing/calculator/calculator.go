package calculator

func Addition(a, b int) int {
	result := a + b
	return result
}

func Subtraction(a, b int) int {
	result := a - b
	return result
}

func Division(a, b float64) float64 {
	result := a / b
	return float64(result)
}

func Multiplication(a, b int) int {
	result := a * b
	return result
}
